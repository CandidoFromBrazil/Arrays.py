"""Adição de elementos em listas"""

"""Para adicionar um elemento ao fim da lista, utilizaremos o método <.append>"""

L = []
print(L)
L.append("Valor_1") #metodo <x.append()>
print(L)
L.append("Valor_2") #metodo <x.append()>
print(L)
L.append("Valor_3") #metodo <x.append()>
print(L)
print(len(L)) #metodo <len()>